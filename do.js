let number = 0; // Initialize the starting point

// Simulate a do-while loop
do {
    if (number % 2 === 0) { // Check if the number is even
        console.log(number); // Print the number if it is even
    }
    number++; // Increment the number
} while (number <= 20); // Loop until the number is less than or equal to 20
